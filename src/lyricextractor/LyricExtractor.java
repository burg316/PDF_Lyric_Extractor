    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Extract Lyrics to text file:
        
      
        
      try {
          
            //Loading an existing document
            File file = new File(jTextField1.getText());
      
      
             //Instantiate PDFTextStripper class
             PDFTextStripper pdfStripper = null;
            
            PDDocument document = PDDocument.load(file);
            
            pdfStripper = new PDFTextStripper();
            
             //Retrieving text from PDF document
            String text = pdfStripper.getText(document);
            
            String[] lineArray = text.split(System.getProperty("line.separator"));
            
            String userDir = System.getProperty("user.home");
            String path = new String(userDir + "/Desktop/Songs/");
            String newName = file.getName().replace(".pdf", "");
            PrintWriter out = new PrintWriter(path + newName + ".txt");
            
            
            
                
            
            for(int i=2; i<lineArray.length; i++){
                if(lineArray[i].contains("LLC")){
                    lineArray[i] = "";
                }else if(lineArray[i].contains("Â©")){
                    lineArray[i] = "";
                }else if(lineArray[i].contains("(Bridge)")){
                    lineArray[i] = System.getProperty("line.separator") + "Bridge";
                }else if(lineArray[i].contains("(Ending)")){
                    lineArray[i] = System.getProperty("line.separator") + "Ending";
                }else if(lineArray[i].contains("(Pre-Chorus)")){
                    lineArray[i] = System.getProperty("line.separator") + "Pre-Chorus";
                }else if(lineArray[i].contains("(TAG)")){
                    lineArray[i] = System.getProperty("line.separator") + "TAG";
                }else if(lineArray[i].contains("(Interlude)")){
                    lineArray[i] = "Interlude";
                }else if(lineArray[i].startsWith("1")){
                    lineArray[i] = "";
                }else if(lineArray[i].contains("Verse 1")){
                    lineArray[i] = System.getProperty("line.separator") + "Verse 1";
                }else if(lineArray[i].contains("Verse 2")){
                    lineArray[i] = System.getProperty("line.separator") + "Verse 2";
                }else if(lineArray[i].contains("Verse 3")){
                    lineArray[i] = System.getProperty("line.separator") + "Verse 3";
                }else if(lineArray[i].contains("Chorus 1")){
                    lineArray[i] = System.getProperty("line.separator") + "Chorus 1";
                }else if(lineArray[i].contains("Chorus 2")){
                    lineArray[i] = System.getProperty("line.separator") + "Chorus 2";
                }else if(lineArray[i].contains("Misc")){
                    lineArray[i] = "";
                }
                out.println(lineArray[i]);
            }
            out.close();
            //Closing the document
             document.close();
             
              JOptionPane.showMessageDialog(null, "Success!");
      } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        
        
      
      
      

     
    }//GEN-LAST:event_jButton2ActionPerformed

